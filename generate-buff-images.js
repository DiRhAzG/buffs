// Prebuild: converts PNG files in src/images/buffs/ to base64 and writes
// src/scripts/buff-images-data.js so the registry can import them without
// any webpack loader configuration.
const fs = require('fs');
const path = require('path');

const imgDir = path.join(__dirname, 'src/images/buffs');
const outFile = path.join(__dirname, 'src/scripts/buff-images-data.js');

const files = fs.readdirSync(imgDir).filter(f => f.endsWith('.png'));

// Group files by buff name (strip trailing _N suffix and .png)
const byName = {};
for (const file of files.sort()) {
    const base = file.replace(/\.png$/, '');
    // Strip variant suffix like _1, _2 etc.
    const name = base.replace(/_\d+$/, '');
    if (!byName[name]) byName[name] = [];
    byName[name].push(file);
}

const lines = ['// AUTO-GENERATED by generate-buff-images.js — do not edit manually', 'export const buffImagesData = {'];
for (const [name, files] of Object.entries(byName)) {
    const base64s = files.map(f => JSON.stringify(fs.readFileSync(path.join(imgDir, f)).toString('base64')));
    lines.push(`    ${JSON.stringify(name)}: [${base64s.join(', ')}],`);
}
lines.push('};');

fs.writeFileSync(outFile, lines.join('\n') + '\n');
console.log(`Generated buff-images-data.js with ${Object.keys(byName).length} buff entries.`);
